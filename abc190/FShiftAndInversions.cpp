using namespace std;

struct BIT {
    int n;
    vector<int> bit; // 1-indexd

    BIT(int sz) {
        bit.resize(sz + 1);
        n = sz;
    }

    int sum(int i) {
        int s = 0;
        while (i > 0) {
            s += bit[i];
            i -= i & -i;
        }
        return s;
    }

    void add(int i, int x) {
        while (i <= n) {
            bit[i] += x;
            i += i & -i;
        }
    }
};


class FShiftAndInversions {
public:
    void solve(std::istream& cin, std::ostream& cout) {
        int N;
        cin >> N;
        vector<int> A(N);
        REP(i, N) {
            cin >> A[i];
            A[i]++;
        }

        BIT bit(N + 1);
        int res = 0;
        REP(i, N) {
            res += A[i] - bit.sum(A[i]) - 1;
            bit.add(A[i], 1);
        }
        REP(i, N) {
            cout << res << endl;
            res += N - 1 - 2 * (A[i] - 1);
        }
    }
};
