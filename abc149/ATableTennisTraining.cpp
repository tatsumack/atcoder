class ATableTennisTraining {
public:
    static constexpr int kStressIterations = 0;

    static void generateTest(std::ostream& test) {
    }

    void solve(std::istream& cin, std::ostream& cout) {
        int n, a, b;
        cin >> n >> a >> b;
        int d = b - a;
        if (d % 2 == 0) {
            cout << d / 2 << endl;
        } else {
            int res = INF;
            {
                int tmp = n - b + 1;
                res = min(res, (n - (a + tmp)) / 2 + tmp);
            }
            {
                int tmp = a - 1 + 1;
                res = min(res, (b - (1 + tmp)) / 2 + tmp);
            }
            cout << res << endl;
        }
    }
};
